USE ROLE ACCOUNTADMIN;
USE DATABASE RECOMART_DB;

SET SOURCE_VALIDATION_BATCH_ID = (
    SELECT SOURCE_VALIDATION_BATCH_ID
    FROM CURATED.USER_ITEM_INTERACTIONS
    WHERE SOURCE_VALIDATION_BATCH_ID IS NOT NULL
    ORDER BY PREPARATION_RUN_TS DESC
    LIMIT 1
);

SET FEATURE_RUN_TS = CURRENT_TIMESTAMP();
SET FEATURE_VERSION = 'v1';

DELETE FROM CURATED.RECOMMENDATION_TRAINING_DATASET
WHERE SOURCE_VALIDATION_BATCH_ID = $SOURCE_VALIDATION_BATCH_ID;

INSERT INTO CURATED.RECOMMENDATION_TRAINING_DATASET (
    USER_ID,
    ITEM_ID,

    USER_TOTAL_VIEWS,
    USER_TOTAL_ADD_TO_CARTS,
    USER_TOTAL_TRANSACTIONS,
    USER_ACTIVITY_LEVEL,
    USER_AVG_IMPLICIT_SCORE,

    ITEM_TOTAL_VIEWS,
    ITEM_TOTAL_ADD_TO_CARTS,
    ITEM_TOTAL_TRANSACTIONS,
    ITEM_POPULARITY_SCORE,
    ITEM_CONVERSION_RATE,
    ITEM_ACTIVITY_LEVEL,

    VIEW_COUNT,
    ADD_TO_CART_COUNT,
    TRANSACTION_COUNT,
    USER_ITEM_IMPLICIT_SCORE,

    TARGET_LABEL,

    SOURCE_VALIDATION_BATCH_ID,
    FEATURE_VERSION,
    FEATURE_RUN_TS
)
SELECT
    ui.USER_ID,
    ui.ITEM_ID,

    ua.TOTAL_VIEWS AS USER_TOTAL_VIEWS,
    ua.TOTAL_ADD_TO_CARTS AS USER_TOTAL_ADD_TO_CARTS,
    ua.TOTAL_TRANSACTIONS AS USER_TOTAL_TRANSACTIONS,
    ua.USER_ACTIVITY_LEVEL,
    ua.AVG_IMPLICIT_SCORE AS USER_AVG_IMPLICIT_SCORE,

    ip.TOTAL_VIEWS AS ITEM_TOTAL_VIEWS,
    ip.TOTAL_ADD_TO_CARTS AS ITEM_TOTAL_ADD_TO_CARTS,
    ip.TOTAL_TRANSACTIONS AS ITEM_TOTAL_TRANSACTIONS,
    ip.ITEM_POPULARITY_SCORE,
    ip.CONVERSION_RATE AS ITEM_CONVERSION_RATE,
    ip.ITEM_ACTIVITY_LEVEL,

    ui.VIEW_COUNT,
    ui.ADD_TO_CART_COUNT,
    ui.TRANSACTION_COUNT,
    ui.IMPLICIT_SCORE AS USER_ITEM_IMPLICIT_SCORE,

    CASE
        WHEN ui.TRANSACTION_COUNT > 0 THEN 1.0
        WHEN ui.ADD_TO_CART_COUNT > 0 THEN 0.7
        WHEN ui.VIEW_COUNT > 0 THEN 0.3
        ELSE 0.0
    END AS TARGET_LABEL,

    $SOURCE_VALIDATION_BATCH_ID,
    $FEATURE_VERSION,
    $FEATURE_RUN_TS
FROM CURATED.USER_ITEM_INTERACTIONS ui
INNER JOIN CURATED.FE_USER_ACTIVITY ua
    ON ui.USER_ID = ua.USER_ID
   AND ua.SOURCE_VALIDATION_BATCH_ID = $SOURCE_VALIDATION_BATCH_ID
INNER JOIN CURATED.FE_ITEM_POPULARITY ip
    ON ui.ITEM_ID = ip.ITEM_ID
   AND ip.SOURCE_VALIDATION_BATCH_ID = $SOURCE_VALIDATION_BATCH_ID
WHERE ui.SOURCE_VALIDATION_BATCH_ID = $SOURCE_VALIDATION_BATCH_ID;