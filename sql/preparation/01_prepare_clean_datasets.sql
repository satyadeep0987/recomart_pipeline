USE ROLE ACCOUNTADMIN;
USE DATABASE RECOMART_DB;

SET SOURCE_VALIDATION_BATCH_ID = (
    SELECT VALIDATION_BATCH_ID
    FROM VALIDATED.RECOMART_EVENTS
    WHERE VALIDATION_BATCH_ID IS NOT NULL
    ORDER BY VALIDATION_RUN_TS DESC
    LIMIT 1
);

SET PREPARATION_RUN_TS = CURRENT_TIMESTAMP();

SELECT
    $SOURCE_VALIDATION_BATCH_ID AS SOURCE_VALIDATION_BATCH_ID,
    $PREPARATION_RUN_TS AS PREPARATION_RUN_TS;

-- Clear prepared data for the current validation batch
DELETE FROM CURATED.CLEAN_RECOMART_EVENTS
WHERE SOURCE_VALIDATION_BATCH_ID = $SOURCE_VALIDATION_BATCH_ID;

DELETE FROM CURATED.CLEAN_RECOMART_ITEM_PROPERTIES
WHERE SOURCE_VALIDATION_BATCH_ID = $SOURCE_VALIDATION_BATCH_ID;

DELETE FROM CURATED.CLEAN_RECOMART_CATEGORY_TREE
WHERE SOURCE_VALIDATION_BATCH_ID = $SOURCE_VALIDATION_BATCH_ID;

DELETE FROM CURATED.CLEAN_EXTERNAL_PRODUCTS
WHERE SOURCE_VALIDATION_BATCH_ID = $SOURCE_VALIDATION_BATCH_ID;


-- ============================================================
-- 1. Clean user behavior events
-- ============================================================

INSERT INTO CURATED.CLEAN_RECOMART_EVENTS (
    EVENT_ID,
    EVENT_TIMESTAMP_MS,
    EVENT_TS,
    EVENT_DATE,
    EVENT_HOUR,
    EVENT_DAY_OF_WEEK,
    USER_ID,
    ITEM_ID,
    EVENT_TYPE,
    EVENT_TYPE_ENCODED,
    EVENT_WEIGHT,
    TRANSACTION_ID,
    SOURCE_FILE_NAME,
    INGESTION_DATE,
    LOAD_TS,
    SOURCE_VALIDATION_BATCH_ID,
    PREPARATION_RUN_TS
)
SELECT
    EVENT_ID,
    EVENT_TIMESTAMP_MS,
    EVENT_TS,
    TO_DATE(EVENT_TS) AS EVENT_DATE,
    DATE_PART(HOUR, EVENT_TS) AS EVENT_HOUR,
    DAYOFWEEK(EVENT_TS) AS EVENT_DAY_OF_WEEK,

    VISITOR_ID AS USER_ID,
    ITEM_ID,
    EVENT_TYPE,

    CASE
        WHEN EVENT_TYPE = 'view' THEN 1
        WHEN EVENT_TYPE = 'addtocart' THEN 2
        WHEN EVENT_TYPE = 'transaction' THEN 3
        ELSE 0
    END AS EVENT_TYPE_ENCODED,

    CASE
        WHEN EVENT_TYPE = 'view' THEN 1.0
        WHEN EVENT_TYPE = 'addtocart' THEN 3.0
        WHEN EVENT_TYPE = 'transaction' THEN 5.0
        ELSE 0.0
    END AS EVENT_WEIGHT,

    TRANSACTION_ID,
    SOURCE_FILE_NAME,
    INGESTION_DATE,
    LOAD_TS,

    VALIDATION_BATCH_ID AS SOURCE_VALIDATION_BATCH_ID,
    $PREPARATION_RUN_TS AS PREPARATION_RUN_TS
FROM VALIDATED.RECOMART_EVENTS
WHERE IS_VALID = TRUE
  AND VALIDATION_BATCH_ID = $SOURCE_VALIDATION_BATCH_ID;


-- ============================================================
-- 2. Clean item properties
-- ============================================================

INSERT INTO CURATED.CLEAN_RECOMART_ITEM_PROPERTIES (
    ITEM_PROPERTY_ID,
    PROPERTY_TS,
    ITEM_ID,
    PROPERTY_NAME,
    PROPERTY_VALUE,
    PROPERTY_VALUE_NUMERIC,
    SOURCE_FILE_NAME,
    INGESTION_DATE,
    LOAD_TS,
    SOURCE_VALIDATION_BATCH_ID,
    PREPARATION_RUN_TS
)
SELECT
    ITEM_PROPERTY_ID,
    PROPERTY_TS,
    ITEM_ID,
    LOWER(TRIM(PROPERTY_NAME)) AS PROPERTY_NAME,
    TRIM(PROPERTY_VALUE) AS PROPERTY_VALUE,
    TRY_TO_DOUBLE(PROPERTY_VALUE) AS PROPERTY_VALUE_NUMERIC,
    SOURCE_FILE_NAME,
    INGESTION_DATE,
    LOAD_TS,
    VALIDATION_BATCH_ID AS SOURCE_VALIDATION_BATCH_ID,
    $PREPARATION_RUN_TS AS PREPARATION_RUN_TS
FROM VALIDATED.RECOMART_ITEM_PROPERTIES
WHERE IS_VALID = TRUE
  AND VALIDATION_BATCH_ID = $SOURCE_VALIDATION_BATCH_ID;


-- ============================================================
-- 3. Clean category tree
-- ============================================================

INSERT INTO CURATED.CLEAN_RECOMART_CATEGORY_TREE (
    CATEGORY_ID,
    PARENT_CATEGORY_ID,
    IS_ROOT_CATEGORY,
    SOURCE_FILE_NAME,
    INGESTION_DATE,
    LOAD_TS,
    SOURCE_VALIDATION_BATCH_ID,
    PREPARATION_RUN_TS
)
SELECT
    CATEGORY_ID,
    PARENT_CATEGORY_ID,
    CASE WHEN PARENT_CATEGORY_ID IS NULL THEN TRUE ELSE FALSE END AS IS_ROOT_CATEGORY,
    SOURCE_FILE_NAME,
    INGESTION_DATE,
    LOAD_TS,
    VALIDATION_BATCH_ID AS SOURCE_VALIDATION_BATCH_ID,
    $PREPARATION_RUN_TS AS PREPARATION_RUN_TS
FROM VALIDATED.RECOMART_CATEGORY_TREE
WHERE IS_VALID = TRUE
  AND VALIDATION_BATCH_ID = $SOURCE_VALIDATION_BATCH_ID;


-- ============================================================
-- 4. Clean external API product data
-- ============================================================

INSERT INTO CURATED.CLEAN_EXTERNAL_PRODUCTS (
    PRODUCT_ID,
    PRODUCT_TITLE,
    PRODUCT_DESCRIPTION,
    CATEGORY,
    PRICE,
    NORMALIZED_PRICE,
    RATING,
    NORMALIZED_RATING,
    STOCK,
    BRAND,
    SOURCE_VALIDATION_BATCH_ID,
    PREPARATION_RUN_TS
)
WITH product_base AS (
    SELECT
        PRODUCT_ID,
        PRODUCT_TITLE,
        PRODUCT_DESCRIPTION,
        CATEGORY,
        PRICE,
        RATING,
        STOCK,
        BRAND,
        VALIDATION_BATCH_ID
    FROM VALIDATED.EXTERNAL_PRODUCTS
    WHERE VALIDATION_BATCH_ID = $SOURCE_VALIDATION_BATCH_ID
),
stats AS (
    SELECT
        MIN(PRICE) AS MIN_PRICE,
        MAX(PRICE) AS MAX_PRICE,
        MIN(RATING) AS MIN_RATING,
        MAX(RATING) AS MAX_RATING
    FROM product_base
)
SELECT
    p.PRODUCT_ID,
    p.PRODUCT_TITLE,
    p.PRODUCT_DESCRIPTION,
    p.CATEGORY,
    p.PRICE,

    CASE
        WHEN s.MAX_PRICE = s.MIN_PRICE THEN 0
        ELSE (p.PRICE - s.MIN_PRICE) / NULLIF((s.MAX_PRICE - s.MIN_PRICE), 0)
    END AS NORMALIZED_PRICE,

    p.RATING,

    CASE
        WHEN s.MAX_RATING = s.MIN_RATING THEN 0
        ELSE (p.RATING - s.MIN_RATING) / NULLIF((s.MAX_RATING - s.MIN_RATING), 0)
    END AS NORMALIZED_RATING,

    p.STOCK,
    p.BRAND,
    p.VALIDATION_BATCH_ID AS SOURCE_VALIDATION_BATCH_ID,
    $PREPARATION_RUN_TS AS PREPARATION_RUN_TS
FROM product_base p
CROSS JOIN stats s;